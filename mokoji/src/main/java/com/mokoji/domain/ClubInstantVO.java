package com.mokoji.domain;

import java.util.Date;

public class ClubInstantVO {
	private String cinst_name, cinst_content;
	private int cinst_code, cinst_memtot, cinst_cost;
	private Date cinst_date;
	
	public String getCinst_name() {
		return cinst_name;
	}
	public void setCinst_name(String cinst_name) {
		this.cinst_name = cinst_name;
	}
	public String getCinst_content() {
		return cinst_content;
	}
	public void setCinst_content(String cinst_content) {
		this.cinst_content = cinst_content;
	}
	public int getCinst_code() {
		return cinst_code;
	}
	public void setCinst_code(int cinst_code) {
		this.cinst_code = cinst_code;
	}
	public int getCinst_memtot() {
		return cinst_memtot;
	}
	public void setCinst_memtot(int cinst_memtot) {
		this.cinst_memtot = cinst_memtot;
	}
	public int getCinst_cost() {
		return cinst_cost;
	}
	public void setCinst_cost(int cinst_cost) {
		this.cinst_cost = cinst_cost;
	}
	public Date getCinst_date() {
		return cinst_date;
	}
	public void setCinst_date(Date cinst_date) {
		this.cinst_date = cinst_date;
	}
	
	
	
}
